    pdf.set_font('helvetica', 'B', 18)
    pdf.set_text_color(200, 255, 200)
    pdf.cell(0, 10, f'SCOPE 3: {total_co2:,.0f} kg CO2e (-18% QoQ)', ln=True, align='C')
    
