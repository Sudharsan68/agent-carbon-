    # Set default text formatting back to 10pt
    pdf.set_font('helvetica', '', 10)

    # ---------------------------------------------------------
    # PAGE 2: CSRD Tables (2 proper tables)
    # ---------------------------------------------------------
    pdf.add_page()
    section_title('CSRD CATEGORY MAPPING')
    
    pdf.set_font('helvetica', 'B', 10)
    pdf.set_fill_color(240, 240, 240)
    pdf.set_text_color(0, 0, 0)
    
    # Explicit table grid
