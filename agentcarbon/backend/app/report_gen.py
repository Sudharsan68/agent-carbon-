from fpdf import FPDF
from datetime import datetime
import hashlib

class ESGReportPDF(FPDF):
    def header(self):
        # Only show header on pages after the cover page
        if self.page_no() > 1:
            self.set_y(5) # Set Y before drawing the rect
            self.set_fill_color(34, 139, 34)
            self.rect(0, 0, 210, 20, 'F')
            self.set_y(8)
            self.set_font('helvetica', 'B', 14)
            self.set_text_color(255, 255, 255)
            self.cell(0, 10, 'AgentCarbon Corporate Sustainability Report', ln=True, align='L')
            self.set_y(25) # push cursor down so content doesn't hit header

    def footer(self):
        # Footer with compliance text and page numbers
        self.set_y(-15)
        self.set_font('helvetica', 'I', 8)
        self.set_text_color(128, 128, 128)
        self.cell(0, 10, 'Generated by AgentCarbon v2.1 | CSRD Phase 2 (2026) Compliant', 0, 0, 'L')
        self.set_x(0)
        self.cell(0, 10, f'Page {self.page_no()}', 0, 0, 'R')

def generate_real_pdf(email, total_co2, total_kwh, facilities, ai_insights):
    pdf = ESGReportPDF()
    
    # ---------------------------------------------------------
    # PAGE 1: COVER PAGE
    # ---------------------------------------------------------
    pdf.add_page()
    pdf.set_fill_color(20, 80, 20)
    pdf.rect(0, 0, 210, 297, 'F')  # Solid dark green background for cover
    
    pdf.set_y(80)
    pdf.set_text_color(255, 255, 255)
    
    # Procedural "Logo" (Geometric Leaf Motif at Top Right)
    pdf.set_fill_color(34, 139, 34)
    pdf.ellipse(165, 30, 15, 15, 'F')
    pdf.set_fill_color(100, 200, 100)
    pdf.ellipse(172, 35, 10, 10, 'F')
    
    pdf.set_font('helvetica', 'B', 32)
    pdf.cell(0, 15, 'AGENTCARBON', ln=True, align='C')
    
    pdf.set_font('helvetica', 'B', 20)
    pdf.cell(0, 15, 'CSRD / ISSB COMPLIANCE REPORT', ln=True, align='C')
    
