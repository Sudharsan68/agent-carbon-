    pdf.cell(0, 8, f'- Fuel: {fu_val:,.0f}kg (31%)', ln=True)
    pdf.cell(10)
    pdf.cell(0, 8, f'- Travel: {tr_val:,.0f}kg (19%)', ln=True)
