        pdf.cell(t_w[1], 8, f" {tm}", 1, 0, 'C')
        pdf.cell(t_w[2], 8, f" {ec}", 1, 0, 'C')
        pdf.cell(t_w[3], 8, f" {nb}", 1, 1, 'C')

